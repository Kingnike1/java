// Define uma classe chamada "hello"
// Em Java, todo código precisa estar dentro de uma classe
public class hello {

    // Método principal (main)
    // É o ponto de entrada do programa
    // Quando você executa o arquivo, é aqui que o Java começa a rodar
    public static void main(String[] args) {

        // System → classe padrão do Java
        // out → objeto responsável pela saída de dados (console)
        // println → método que imprime texto e pula para a próxima linha
        System.out.println("Hello, World!");
    }
}
